	/* init hardware pins */
	nrf24_init();
	
	/* Channel #2 , payload length: 4 */
	nrf24_config(2,4);

	/* Set the device addresses */
	nrf24_tx_address(tx_address);
	nrf24_rx_address(rx_address);    

  /* USER CODE END 2 */

  /* Infinite loop */
